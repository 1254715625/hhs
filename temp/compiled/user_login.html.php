function showLogin(){
	$('.logo').show();
	$('#dregister').hide();
	$('#dbrowse').hide();
	$('#header .header').animate({'top':125}, 300);
	$('#dlogin').show();
	$('#dlogin').attr('login',2);
	$('#dregister').attr('reg',1);
	$('#dbrowse').attr('browse',1);
	$('#lusername').focus();				
} 

function showRegister(){
	$('.logo').hide();
	$('#dlogin').hide();
	$('#dbrowse').hide();
	$('#header .header').animate({ 'top':-150}, 300);
	$('#dregister').show().animate({ 'top':120}, 300);
	$('#dregister').attr('reg',2);
	$('#dlogin').attr('login',1);
	$('#dbrowse').attr('browse',1);
	$('#username').focus();
}

function showBrowse(){
	$('.logo').show();
	$('#dlogin').hide();
	$('#dregister').hide();
	$('#dbrowse').hide();
	$('#header .header').animate({ 'top':125}, 300);
	$('#dbrowse').show();
	$('#dbrowse').attr('browse',2);
	$('#dlogin').attr('login',1);
	$('#dregister').attr('reg',1);
}
